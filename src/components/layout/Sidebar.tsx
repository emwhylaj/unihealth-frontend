export default function Sidebar() {
  const pathname = usePathname();
  const role = useAuthStore((s) => s.user?.role);

  const navItems =
    role === "Admin" ? adminNavItems :
    role === "Hospital" ? hospitalNavItems :
    patientNavItems;

  const roleLabel =
    role === "Admin" ? "Admin Panel" :
    role === "Hospital" ? "Hospital Portal" :
    null;

  return (
    <aside className="w-64 min-h-screen bg-[#0f1f0f] border-r border-[#1a2e1a] flex flex-col">
      {/* Logo */}
      <div className="p-6 border-b border-[#1a2e1a]">
        <Link href="/" className="flex items-center gap-3">
          <UniHealthLogo size="sm" showText={false} />
          <span className="text-[#4ade80] font-bold text-sm tracking-widest uppercase">
            UNIHEALTH
          </span>
        </Link>
      </div>

      {roleLabel && (
        <div className="px-4 pt-4">
          <span className="text-xs font-semibold text-[#4ade80]/60 uppercase tracking-widest px-4">
            {roleLabel}
          </span>
        </div>
      )}

      {/* Navigation */}
      <nav className="flex-1 p-4 space-y-1">
        {navItems.map((item) => {
          const isActive =
            item.href === "/dashboard"
              ? pathname === "/dashboard"
              : pathname.startsWith(item.href);

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-4 py-3 rounded-xl text-sm font-medium transition-all duration-200 ${
                isActive
                  ? "bg-[#4ade80]/10 text-[#4ade80] border border-[#4ade80]/20"
                  : "text-gray-400 hover:text-[#4ade80] hover:bg-[#4ade80]/5"
              }`}
            >
              <span className={isActive ? "text-[#4ade80]" : "text-gray-500"}>
                {item.icon}
              </span>
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-[#1a2e1a]">
        <p className="text-xs text-gray-600 text-center">
          UniHealth &copy; {new Date().getFullYear()}
        </p>
      </div>
    </aside>
  );
}
